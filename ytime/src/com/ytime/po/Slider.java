package com.ytime.po;

public class Slider {
	
	private int sliderID;
	
	private String sliderName;
	
	private String sliderSubject;
	
	private String sliderDesc;
	
	private String sliderImg;
	
	private int sflag;
	
	private int sorder;

	public int getSliderID() {
		return sliderID;
	}

	public void setSliderID(int sliderID) {
		this.sliderID = sliderID;
	}

	public String getSliderName() {
		return sliderName;
	}

	public void setSliderName(String sliderName) {
		this.sliderName = sliderName;
	}

	public String getSliderSubject() {
		return sliderSubject;
	}

	public void setSliderSubject(String sliderSubject) {
		this.sliderSubject = sliderSubject;
	}

	public String getSliderDesc() {
		return sliderDesc;
	}

	public void setSliderDesc(String sliderDesc) {
		this.sliderDesc = sliderDesc;
	}

	public String getSliderImg() {
		return sliderImg;
	}

	public void setSliderImg(String sliderImg) {
		this.sliderImg = sliderImg;
	}

	public int getSflag() {
		return sflag;
	}

	public void setSflag(int sflag) {
		this.sflag = sflag;
	}

	public int getSorder() {
		return sorder;
	}

	public void setSorder(int sorder) {
		this.sorder = sorder;
	}

}
